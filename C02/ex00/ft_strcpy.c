char	*ft_strcpy(char *dest, char *src)
{
	char	*n;

	n = dest;
	while (*src != '\0')
	{
		*dest = *src;
		src++;
		dest++;
	}
	*dest = '\0';
	dest = n;
	return (dest);
}
